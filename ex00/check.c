int	count_check(int argc, char **argv)
{
	int	n;
	int	x;
	int	temp;

	x = 1;
	n = 0;
	if (argc != 2)
		return (0);
	while ((argv[1][n] <= '9') && (argv[1][n] >= '0')
			&& (argv[1][n + 1] == ' '))
	{
		x++;
		n += 2;
		if ((argv[1][n] > '9')
				&& (argv[1][n] < '0'))
			return (0);
	}
	temp = x % 4;
	if ((temp != 0) || (n > 72))
		return (0);
	else
		return (x / 4);
}
